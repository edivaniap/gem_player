    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        jLabelMessageActions.setVisible(false);
        clearCadastro();

        //getSelectedRow() : Returns the index of the first selected row, -1 if no row is selected.
        int selectedRowIndex = jTableUsers.getSelectedRow();

        if (selectedRowIndex != -1) {
            jLabelTitleAttach.setText("Editar usuário");
            jButtonEnter.setText("Editar");
            jPanelCadastro.setVisible(true);

            userDAO = new UserDAO();
            currentSelectedUser = tableModel.getValueAt(selectedRowIndex, 1).toString();
            String currentType =tableModel.getValueAt(selectedRowIndex, 2).toString();
            String currentPass = userDAO.getPassword(currentSelectedUser);
            

            jTextName.setText(tableModel.getValueAt(selectedRowIndex, 0).toString());
            jTextUsername.setText(tableModel.getValueAt(selectedRowIndex, 1).toString());
            jPasswordField.setText(currentPass);
            jPasswordFieldConfirm.setText(currentPass);
            
            if(currentType.equals("VIP")) {
                jCheckBoxVIP.setSelected(true);
            } else {
                jCheckBoxVIP.setSelected(false);
            }
        } else {
            jLabelMessageActions.setText("Selecione um usuário para editar");
            jLabelMessageActions.setVisible(true);
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

