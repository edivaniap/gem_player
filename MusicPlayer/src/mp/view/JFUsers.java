    private void jButtonRemovActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemovActionPerformed
        jLabelMessageActions.setVisible(false);

        //o index vai indicar o numero da linha que deve ser exluida
        //getSelectedRow() : Returns the index of the first selected row, -1 if no row is selected.
        int selectedRowIndex = jTableUsers.getSelectedRow();

        if (selectedRowIndex != -1) {
            userDAO.delete(selectedRowIndex);
            loadUsersOnJTable();
            jLabelMessageActions.setText("Usuário excluido com sucesso");
        } else {
            jLabelMessageActions.setText("Selecione um usuário para excluir");
        }
        jLabelMessageActions.setVisible(true);
    }//GEN-LAST:event_jButtonRemovActionPerformed

    private void jButtonEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnterActionPerformed
        jLabelMessageErros.setVisible(false);
        jLabelMessageActions.setVisible(false);

        boolean isUnfilled = jTextUsername.getText().equals("") || jTextUsername.getText().equals("")
                || jPasswordField.getText().equals("") || jPasswordFieldConfirm.getText().equals("");

        boolean passwordConfirmation = jPasswordField.getText().equals(jPasswordFieldConfirm.getText());

        boolean alreadyExistUser = userDAO.alreadyExist(jTextUsername.getText());

        if (isUnfilled) {
            jLabelMessageErros.setText("Preencha todos os campos");
            jLabelMessageErros.setVisible(true);
        } else if (!passwordConfirmation) {
            jLabelMessageErros.setText("As senhas não correspondem");
            jLabelMessageErros.setVisible(true);
        } else if (alreadyExistUser) {
            jLabelMessageErros.setText("Já temos alguém usando este usuário");
            jLabelMessageErros.setVisible(true);
        } else {
            User user;
            
            if (jCheckBoxVIP.isSelected()) {
                user = new VIPUser(jTextName.getText(), jTextUsername.getText(), jPasswordField.getText());
            } else {
                user = new CommonUser(jTextName.getText(), jTextUsername.getText(), jPasswordField.getText());
            }

            if (jButtonEnter.getText().equals("Cadastrar")) {
                userDAO.insert(user);
                jLabelMessageActions.setText("Usuário adicionado com sucesso");
            } else {
                userDAO.edit(user, currentSelectedUser);
                jLabelMessageActions.setText("Usuário editado com sucesso");
            }

            clearCadastro();
            loadUsersOnJTable();
            jLabelMessageActions.setVisible(true);
        }
    }//GEN-LAST:event_jButtonEnterActionPerformed

